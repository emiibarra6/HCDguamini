	<div class="wpb_text_column wpb_content_element ">
		<div class="wpb_wrapper">
			<div style="font-size: 14px;" align="justify">Los proyectos que se presenten al Concejo podrán ser:<br>
<strong>1) ORDENANZA:</strong> Si crea, reforma, suspende o deroga una regla general, cuyo cumplimiento compete a la Intendencia Municipal.-<br>
<strong>2) RESOLUCIÓN:</strong> Si tiene por objeto el rechazo o aceptación de solicitudes particulares, la adopción de medidas relativas a la composición u organización interna del Concejo; manifestar su voluntad de practicar algún acto en tiempo determinado.-<br>
<strong>3) COMUNICACIÓN:</strong> Si tiene por objeto contestar, pedir, recomendar o exponer algo.-<br>
<strong>4) DECLARACIÓN: </strong>Cuando tenga por objeto expresar una opinión del Concejo sobre cualquier asunto de carácter público o privado.-<br>
