	<div class="wpb_text_column wpb_content_element ">
		<div class="wpb_wrapper">
			<div style="font-size: 14px;" align="justify">La oficina Diario de Sesiones del Concejo tiene la misión de transcribir todo lo que se dice en las sesiones: debates, nómina de todos los asuntos que tienen entrada al cuerpo mediante expedientes, la transcripción de los proyectos con los nombres de los respectivos autores, etc.<br>
