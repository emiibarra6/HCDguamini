	<div class="wpb_text_column wpb_content_element ">
		<div class="wpb_wrapper">
			<div style="font-size: 14px;" align="justify">El Concejo Deliberante se compone de doce (12) concejales – esto se calcula por la cantidad de habitantes de la ciudad según (Capítulo I – Art. 2º de la Ley Orgánica de las Municipalidades), elegidos por el voto popular, por un período de cuatro (4) años, y se renueva por mitades cada dos años y podrán ser reelectos..<br>
Estos concejales eligen entre sus pares sus autoridades (Presidente, Vicepresidente 1° y Vicepresidente 2°), y a propuesta de los distintos Bloque Políticos el Secretario.</div>

