	<div class="wpb_text_column wpb_content_element ">
		<div class="wpb_wrapper">
			<div style="font-size: 14px;" align="justify">Los Concejales electos tomarán posesión de sus cargos en la fecha que legalmente corresponda a la renovación de autoridades.<br>
En la fecha fijada por la Junta Electoral, se reunirá el Concejo Deliberante en sesiones preparatorias, integrado por los nuevos electos, diplomados por aquélla y los Concejales que no cesen en sus mandatos y procederán a establecer si los primeros reúnen las condiciones exigidas por la Constitución de la Provincia y esta ley. Las sesiones serán presididas por el Concejal de mayor edad de la lista triunfante.<br>
Los concejales se convocan a través de sesiones, además de las reuniones de comisión que cada una de ésta propone.<br>
El Concejo tendrá cinco (5) Comisiones Internas designadas de acuerdo a la Ley Orgánica Municipal.-<br>
Las Comisiones permanentes serán las que a continuación se expresan:<br>
1) HACIENDA<br>
2) PRESUPUESTO Y CUENTAS<br>
3) OBRAS PUBLICAS Y SEGURIDAD<br>
4) INTERPRETACIÓN Y REGLAMENTO<br>
5) SALUD PÚBLICA, ASISTENCIA SOCIAL Y CULTURA.</div>

