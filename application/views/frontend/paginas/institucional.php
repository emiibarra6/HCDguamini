	<div class="wpb_text_column wpb_content_element ">
		<div class="wpb_wrapper">
			<div style="font-size: 14px;" align="justify">El Concejo realizará sesiones con el carácter y en los términos que a continuación se indican:<br>
<strong>1°. – Preparatorias:</strong> En la fecha fijada por la Junta Electoral, para cumplir lo dispuesto en los artículos 18 al 23 de la presente.<br>
<strong>2°. – Ordinarias:</strong> Por propia determinación abrirá sus Sesiones Ordinarias el 1° de abril de cada año y las cerrará el 30 de noviembre.<br>
<strong>3°. – De Prórroga:</strong> El Concejo podrá prorrogar las Sesiones Ordinarias por el término de treinta (30) días.<br>
<strong>4°. – Especiales:</strong> Las que determine el Cuerpo dentro del período de sesiones ordinarias y de prórroga, y las que deberá realizar en el mes de marzo por propia determinación, para tratar el examen de las cuentas, previsto en el artículo 192 inciso 5°(*), de la Constitución<br>
<em>(*) Texto constitucional corresponde a reforma 1994.</em><br>
<strong>5°. -Extraordinarias</strong>: El Concejo podrá ser convocado por el Intendente a sesiones extraordinarias, siempre que un asunto de interés público y urgente lo exija, o convocarse por sí mismo cuando, por la misma razón, lo solicite un mínimo de un tercio del número de sus miembros.<br>
<em>(Ley Orgánica de las Municipalidades -Cap. II – ART. 68° – Ley 11.690)</em></div>

